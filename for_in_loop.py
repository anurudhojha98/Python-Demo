


"""For In Loop for lists"""

list=[3,4,5,6,3,"anu",3.4]

for listItem in list:
    print(listItem)

'''For In loop for String'''

for char in "Anurudh":
    print(char)

"""For In Loop for Set"""

setItem={"anu",4,4,5.5,2}
for item in setItem:
    print(item)

"""For In Loop for dict"""

data={
    "name":"anu",
    "age":21
}

for k,v in data.items():
    print(k,v)


